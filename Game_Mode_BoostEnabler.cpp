///*****************************************************************
//GAME - MODE - BOOSTENABLER - CONSTRUCTORS
///*****************************************************************
Game_Mode_BoostEnabler::Game_Mode_BoostEnabler(void)
{
	//-------------------------------------------------------------
	EnableTimer = 0;
	EnableTimerMax = 0;
	//-------------------------------------------------------------
}

Game_Mode_BoostEnabler::~Game_Mode_BoostEnabler(void)
{
	//-------------------------------------------------------------
	//-------------------------------------------------------------
}
///*****************************************************************
//GAME - MODE - BOOSTENABLER - CREATE
///*****************************************************************
void Game_Mode_BoostEnabler::Create(void)
{
	//-------------------------------------------------------------
	EnableTimer = 0;
	//EnableTimerMax = 600; //10seconds
	EnableTimerMax = 1200; 
	//-------------------------------------------------------------
}
///*****************************************************************
//GAME - MODE - BOOSTENABLER - UPDATE
///*****************************************************************
void Game_Mode_BoostEnabler::Update(void)
{
	//-------------------------------------------------------------
	if(!gBoostEnable && gBikesGo)
	{
		if(EnableTimer<EnableTimerMax)
		{
			EnableTimer++;
		}
		else
		{
			gBoostEnable = true;
		}
	}
	//-------------------------------------------------------------
}
///*****************************************************************
//GAME - MODE - BOOSTENABLER - DESTROY
///*****************************************************************
void Game_Mode_BoostEnabler::Destroy(void)
{
	//-------------------------------------------------------------
	EnableTimer = 0;
	EnableTimerMax = 0;
	//-------------------------------------------------------------
}