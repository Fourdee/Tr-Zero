///*****************************************************************
//GAME - MODE - NET ARENA - CONSTRUCTORS
///*****************************************************************
Game_Mode_Arena::Game_Mode_Arena(void)
{
	//-------------------------------------------------------------
	ZeroData();
	//-------------------------------------------------------------
}

Game_Mode_Arena::~Game_Mode_Arena(void)
{
	//-------------------------------------------------------------
	//-------------------------------------------------------------
}
///*****************************************************************
///GAME - MODE - NET ARENA - ZERODATA
///*****************************************************************
void Game_Mode_Arena::ZeroData(void)
{
	//-------------------------------------------------------------
	TimerMax = 18000;//3600 = 1 minute
	//-------------------------------------------------------------
}
///*****************************************************************
//GAME - MODE - NET ARENA - CREATE
///*****************************************************************
void Game_Mode_Arena::Create(void)
{
	//-------------------------------------------------------------
	ZeroData();
	//-------------------------------------------------------------
	//Apply Global Timer
	gRaceCountdown = TimerMax;
	//-------------------------------------------------------------
}
///*****************************************************************
//GAME - MODE - NET ARENA - UPDATE
///*****************************************************************
void Game_Mode_Arena::Update(void)
{
	//-------------------------------------------------------------
	//Update Timer
	if(gRaceCountdown>0)
	{
		gRaceCountdown--;
	}
	//-------------------------------------------------------------
}